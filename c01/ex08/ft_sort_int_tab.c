void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		a;
	int		temp;

	a = size - 1;
	while (a > 0)
	{
		i = 0;
		while (i < a)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
		a--;
	}
}
